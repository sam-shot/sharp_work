import 'package:flutter/material.dart';

class SharpPadding {
  SharpPadding._();

  static EdgeInsets get horizontalPadding16 =>
      EdgeInsets.symmetric(horizontal: 16);

  static EdgeInsets get horizontalPadding20 =>
      EdgeInsets.symmetric(horizontal: 20);

  static EdgeInsets get verticalPadding16 => EdgeInsets.symmetric(vertical: 16);

  static EdgeInsets get verticalPadding20 => EdgeInsets.symmetric(vertical: 20);

  static EdgeInsets get padding16 => EdgeInsets.all(16);

  static EdgeInsets get padding20 => EdgeInsets.all(20);
}

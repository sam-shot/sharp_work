import 'package:flutter/material.dart';
import 'package:flutter_screenutil/flutter_screenutil.dart';

class SharpPadding {
  SharpPadding._();

  static EdgeInsets get horizontalPadding16 =>
      EdgeInsets.symmetric(horizontal: 16.r);

  static EdgeInsets get horizontalPadding20 =>
      EdgeInsets.symmetric(horizontal: 20.r);

  static EdgeInsets get verticalPadding16 =>
      EdgeInsets.symmetric(vertical: 16.r);

  static EdgeInsets get verticalPadding20 =>
      EdgeInsets.symmetric(vertical: 20.r);

  static EdgeInsets get padding4 => EdgeInsets.all(4.r);

  static EdgeInsets get padding6 => EdgeInsets.all(6.r);

  static EdgeInsets get padding8 => EdgeInsets.all(8.r);

  static EdgeInsets get padding10 => EdgeInsets.all(10.r);

  static EdgeInsets get padding16 => EdgeInsets.all(16.r);

  static EdgeInsets get padding20 => EdgeInsets.all(20.r);
}

import 'package:gap/gap.dart';

class SharpSpacing {
  SharpSpacing._();

  static Gap get tiny => Gap(2);
  static Gap get small => Gap(4);
  static Gap get medium => Gap(8);
  static Gap get normal => Gap(16);
  static Gap get large => Gap(24);
  static Gap get xLarge => Gap(32);
  static Gap get xxLarge => Gap(48);
  static Gap get xxxLarge => Gap(64);
}
